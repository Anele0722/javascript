// Declare and assign the variable 'favoriteColour'
let favoriteColour = 'blue';

// Print the initial value of favoriteColour
console.log(favoriteColour); // Outputs: blue

// Function to reassign favoriteColour
function changeColor(newColor) {
    favoriteColour = newColor;
}

// Call the function to change the color
changeColor('white');

// Print the updated value of favoriteColour
console.log(favoriteColour); // Outputs: white
