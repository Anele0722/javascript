// Declare a number variable and assigning it a value
let num1 = 10;

// Declare a floating number variable
let num2 = 2.14;

// Perform arithmetic operations
let sum = num1 + 5;                // Adds 5 to num1, resulting in 15
let difference = num2 - 1;         // Subtracts 1 from num2, resulting in 2.14
let product = num1 * 2;            // Multiplies num1 by 2, resulting in 20
let quotient = num2 / 1.5;         // Divides num2 by 1.5, resulting in approximately 2.0933333333333333
let modulus = num1 % 3;            // Computes the remainder of num1 divided by 3, resulting in 1
let exponentiation = num1 ** 2;    // Raises num1 to the power of 2, resulting in 100

// Print the results
console.log("Sum:", sum);                        // Outputs: Sum: 15
console.log("Difference:", difference);          // Outputs: Difference: 2.14
console.log("Product:", product);                // Outputs: Product: 20
console.log("Quotient:", quotient);              // Outputs: Quotient: 2.0933333333333333
console.log("Modulus:", modulus);                // Outputs: Modulus: 1
console.log("Exponentiation:", exponentiation);  // Outputs: Exponentiation: 100
